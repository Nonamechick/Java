        int left = 0, right = N - 1;
        left = 0;
        right = N - 1;
        while (left < right) {
            System.out.print(array[left] + " ");
            System.out.print(array[left + 1] + " ");
            System.out.print(array[right] + " ");
            System.out.print(array[right - 1] + " ");
            left += 2;
            right -= 2;
        }
        if (left == right) { 
            System.out.print(array[left] + " ");
        }
        System.out.println();

